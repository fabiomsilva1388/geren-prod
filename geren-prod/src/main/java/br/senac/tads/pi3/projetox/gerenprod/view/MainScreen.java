    private void btn_AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarActionPerformed
        Exceptions.limparMensagesErro();

        boolean status = true;

        Exceptions.validarCampoTexto(txt_NomeProduto);
        Exceptions.validarCampoTexto(txt_Descricao);
        Exceptions.validarDecimal(txt_PrecoCompra);
        Exceptions.validarDecimal(txt_PrecoVenda);
        Exceptions.validarNumero(txt_Quantidade);
        if (Exceptions.getMensagensErro().size() > 0) {
            String erros = "";
            for (String erro : Exceptions.getMensagensErro()) {
                erros += erro + "<br/>";
            }

            JOptionPane.showMessageDialog(null, "Preencha os campos em vermelho!");
        } else {

            String item = (String) ckb_Status.getSelectedItem();
            if (!item.equals("Ativo")) {
                status = false;
            }
            
            
            
            ProdutoController.Atualizar(txt_NomeProduto.getText(), txt_Descricao.getText(), Double.parseDouble(txt_PrecoCompra.getText()), Double.parseDouble(txt_PrecoVenda.getText()), Integer.parseInt(txt_Quantidade.getText()), status,Integer.parseInt(tbl_Tabela.getValueAt(tbl_Tabela.getSelectedRow(),0).toString()));

            JOptionPane.showMessageDialog(null, "Operação realizada com sucesso!");
        }


    }//GEN-LAST:event_btn_AtualizarActionPerformed

