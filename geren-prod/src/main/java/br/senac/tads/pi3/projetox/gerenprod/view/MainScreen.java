    public void LoadTable(String nome){

        ArrayList<String[]> linhasProdutos = ProdutoController.consultarPorNome(nome);

        DefaultTableModel tmProdutos = new DefaultTableModel();
        tmProdutos.addColumn("ID");
        tmProdutos.addColumn("NOME");
        tmProdutos.addColumn("DESCRICAO");
        tmProdutos.addColumn("PRECO_COMPRA");
        tmProdutos.addColumn("PRECO_VENDA");
        tmProdutos.addColumn("QUANTIDADE");
        tbl_Tabela.setModel(tmProdutos);

        for (String[] c : linhasProdutos) {
            tmProdutos.addRow(c);
        }
}

    
